    private void cmbKategoriActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbKategoriActionPerformed
        // TODO add your handling code here:
        cmbSnack.removeAllItems();
        String kategori = (String) cmbKategori.getSelectedItem();
        if (kategori.equals("Snack Import")) {
            cmbSnack.addItem("Choco Crunch");
            cmbSnack.addItem("Matcha Wafer");
            cmbSnack.addItem("Nori Stick");
            cmbSnack.addItem("Pocky Strawberry");
            cmbSnack.addItem("Kinder Bueno");
        } else {
            cmbSnack.addItem("Cheese Stick");
            cmbSnack.addItem("Taro Chips");
            cmbSnack.addItem("Singkong Crispy");
            cmbSnack.addItem("Keripik Tempe");
            cmbSnack.addItem("Keripik Pisang");
        }
    }//GEN-LAST:event_cmbKategoriActionPerformed

