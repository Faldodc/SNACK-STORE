    private void btnBeliActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBeliActionPerformed
        // TODO add your handling code here:
        try {
            String nama = txtName.getText();
            String tipePembeli = cmbTipe.getSelectedItem().toString();
            String kategori = "Snack Import";
            int jumlah = Integer.parseInt(txtJumlah.getText());
            String snack = cmbSnack.getSelectedItem().toString();
            boolean promo = cbPromo.isSelected();

            double harga = switch (snack) {
                case "Choco Crunch" -> 8000;
                case "Matcha Wafer" -> 9500;
                case "Nori Stick" -> 11000;
                case "Pocky Strawberry" -> 10500;
                case "Kinder Bueno" -> 12000;
                default -> 7000;
            };

            double total = harga * jumlah;
            if (promo) total *= 0.8;
            if (tipePembeli.equals("Member")) total *= 0.9;

            DefaultTableModel model = (DefaultTableModel) tabel.getModel();
            model.addRow(new Object[]{
                nama, 
                tipePembeli, 
                "Snack Import",
                kategori, 
                jumlah, 
                snack, 
                "Rp" + harga, 
                promo ? "Ya" : "Tidak"
            });

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Input error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnBeliActionPerformed

